.DATA
PROMPTA DB 'ENTER FIRST NUMBER A1: $'
PROMPTB DB 'ENTER SECOND NUMBER A2: $'
OUTPUT DB 'RESULT: $'
NEWLINE DB 10, 13, '$'
A1 DW ?
A2 DW ?
RESULT DW ?
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX
MOV AH, 09H
MOV DX, OFFSET PROMPTA
INT 21H
MOV AH, 01H
INT 21H
SUB AL, '0'
MOV A1, AX
MOV AH, 02H
MOV DL, 0AH
INT 21H
MOV AH, 02H
MOV DL, 0DH
INT 21H
MOV AH, 09H
MOV DX, OFFSET PROMPTB
INT 21H
MOV AH, 01H
INT 21H
SUB AL, '0'
MOV A2, AX
MOV AH, 02H
MOV DL, 0AH
INT 21H
MOV AH, 02H
MOV DL, 0DH
INT 21H
MOV AX, A1
AND AX, 1
CMP AX, 0
JE EVEN
; PERFORM DIVISION
XOR DX, DX
MOV AX, A1
MOV BX, A2
DIV BX
MOV RESULT, AX
JMP DISPLAY_RESULT
EVEN:
; PERFORM MULTIPLICATION
MOV AX, A1
MUL A2
MOV RESULT, AX
DISPLAY_RESULT:
MOV AH, 09H
MOV DX, OFFSET OUTPUT
INT 21H
MOV AX, RESULT
ADD AX, 3030H
MOV DX, AX
MOV AH, 02H
INT 21H
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN